    private void setRealEstateInfo(RealEstate realEstate) {
        jTxtOwner.setText(realEstate.getOwnerAddress());
        jTxtLocation.setText(realEstate.getRealEstateAddress());
        jTxtArea.setText(Integer.toString(realEstate.getArea()));
        jTxtDistance.setText(Integer.toString(realEstate.getCenterDistance()));
        jTxtPrice.setText(Integer.toString(realEstate.getPrice()));

        jTxtOwner.setEnabled(false);
        jTxtLocation.setEnabled(false);
        jTxtArea.setEnabled(false);
        jTxtDistance.setEnabled(false);
        jTxtPrice.setEnabled(false);
    }

    private void centerForm() {
        setLocationRelativeTo(null);
    }
